function next1a() {
    var x = document.getElementById("game0");
    var y = document.getElementById("game1a");
    if (x.style.display === "block") {
      x.style.display = "none";
      y.style.display = "block";
    } else {
      x.style.display = "block";
      y.style.display = "none";
    }
  }
